class NestedIterator(object):

    def __init__(self, nestedList):
        """
        Initialize your data structure here.
        :type nestedList: List[NestedInteger]
        """
        self.d = collections.deque()

        def process(nested):
            for n in nested:
                # return n
                if n.isInteger():
                    self.d.append(n)
                else:
                    process(n.getList())

        process(nestedList)

    def next(self):
        """
        :rtype: int
        """
        return self.d.popleft()

    def hasNext(self):
        """
        :rtype: bool
        """
        return len(self.d)

